    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Usuario us = new Usuario();
        us.setIdUsuario(this.p.getIdPaciente());
        us.setPaciente(p);
        us.setUsuarioUsuario(txtNome.getText());
        us.setSenhaUsuario(txtSenha.getText());
        if(status==1)
        {
            try {
                usDao.create(us);
                JOptionPane.showMessageDialog(this, "Usuario criado");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "erro : "+ex);
            }
        }
        else if(status==2)
        {
            try {
            usDao.edit(us);
            JOptionPane.showMessageDialog(this, "Usuario Atualizado");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "erro : "+ex);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

