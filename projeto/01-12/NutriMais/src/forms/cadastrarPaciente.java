    private void preencherTabelaUm(Paciente p)
    {

            DefaultTableModel tabelapac = (DefaultTableModel)tblPaciente.getModel();
            tabelapac.setNumRows(0);

            Object[] obj = new Object[]{
                p.getIdPaciente(),
                p.getNome()
            };
            tabelapac.addRow(obj);
    }
    
    private void preencherTabela(List<Paciente> lista)
    {
        if(lista.size()>0)
        {
            DefaultTableModel tabelapac = (DefaultTableModel)tblPaciente.getModel();
            tabelapac.setNumRows(0);
            for(Paciente p : lista)
            {
                Object[] obj = new Object[]{
                  p.getIdPaciente(),
                  p.getNome()
                };
                tabelapac.addRow(obj);
            }
        }
    }
        
    private void preencheCampos()
    {
        int linhaSelecionada  = tblPaciente.getSelectedRow();
        if(linhaSelecionada != -1)
        {
            int idPaciente = Integer.parseInt(tblPaciente.getValueAt(linhaSelecionada, 0).toString());
            Paciente f = pacDAO.findPaciente(idPaciente);
            txtNome.setText(f.getNome());
            txtEndereco.setText(f.getEndereco());
            txtBairro.setText(f.getBairro());
            txtCidade.setText(f.getCidade());
            txtCEP.setText(f.getCep());
            txtTelefone.setText(f.getTelefone());      
            cmbEtnia.setSelectedIndex(0);
            cmbSexo.setSelectedIndex(0); 
            txtIndicacao.setText(f.getIndicacao());
            txtMotivoVisita.setText(f.getMotivo());
            txtEmail.setText(f.getEmail());
            txtMedico.setText(f.getMedico());
            txtConvenio.setText(f.getConvMedico());
            txtCart.setText(f.getCart());
            txtHospital.setText(f.getHospital());
        }
    }
