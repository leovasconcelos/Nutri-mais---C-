    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada  = tblPaciente.getSelectedRow();
        if(linhaSelecionada != -1)
        {
            int idPaciente = Integer.parseInt(tblPaciente.getValueAt(linhaSelecionada, 0).toString());
            int confirma = JOptionPane.showConfirmDialog(this, "deseja realmente excluir");
            if(confirma == 0)
            {           
                try{
                    pacDAO.destroy(idPaciente);
                    JOptionPane.showMessageDialog(this, "Paciente excluido com sucesso");
                    limpaCampos();
                    preencherTabela(pacDAO.findPacienteEntities()); 
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, "Erro ao excluir: "+e.getMessage());
                } 
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

